# Lists are like arrays.

# Declaring and initializing an empty lists.


# Unlike arrays, we can increase and decrease the size without 
# having to declare it again.
my_list = []

# Using .append() we will put an element to the back of the list
my_list.append(9)

# A lists can contain any data type
my_list.append("A string")

# Yes, I said it. Any kind of data type.
my_list.append(7.9)

# And yes, we can access like array. Isn't it great?
print(my_list[0])
print(my_list[1])
print(my_list[2])
